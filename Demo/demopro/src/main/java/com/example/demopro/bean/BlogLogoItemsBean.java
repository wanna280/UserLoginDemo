package com.example.demopro.bean;

public class BlogLogoItemsBean {
    private int id;
    private int watching_numbers;
    private int comment_numbers;
    private int thumbsup_numbers;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getWatching_numbers() {
        return watching_numbers;
    }

    public void setWatching_numbers(int watching_numbers) {
        this.watching_numbers = watching_numbers;
    }

    public int getComment_numbers() {
        return comment_numbers;
    }

    public void setComment_numbers(int comment_numbers) {
        this.comment_numbers = comment_numbers;
    }

    public int getThumbsup_numbers() {
        return thumbsup_numbers;
    }

    public void setThumbsup_numbers(int thumbsup_numbers) {
        this.thumbsup_numbers = thumbsup_numbers;
    }
}
